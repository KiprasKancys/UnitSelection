void removeCharsFromString(string &str, char* charsToRemove) {
	for (unsigned int i = 0; i < strlen(charsToRemove); ++i) {
		str.erase(remove(str.begin(), str.end(), charsToRemove[i]), str.end());
	}
}

int trText2UnitList(char *TrSakinys, unsigned short *units, unsigned short *unitseparators)
{
	char s[200], *pos;
	strcpy(s, TrSakinys);

	int i=0;
	pos=strtok(s, "+- ");
	string previous = string(pos);
	pos = strtok(NULL, "+- ");
	string next;
	string both;

	while(pos!=NULL)
	{
		next = string(pos);
		pos = strtok(NULL, "+- \n");
		both = previous + " " + next;
		previous = next;
		
		char *cstr = new char[both.length() + 1];
		strcpy(cstr, both.c_str());			
		units[i] = fv2id(cstr);
		delete[] cstr;
		
		i++;
	}

	return i;
}